function calcularpromedio(promedio1, promedio2, promedio3) {
    var promedio = (promedio1 + promedio2 + promedio3) / 3;
    return promedio;
}
var calcular = 0;
calcular = calcularpromedio(100, 90, 90);
console.log(calcular);
